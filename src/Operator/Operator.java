package Operator;

public class Operator {
    public static void main(String[] args) {
        int a = 3;
        int b = 7;
        double jumlah;
        jumlah = a + b;
        System.out.println("a + b = " + jumlah);
        jumlah = a - b;
        System.out.println("a - b = " +jumlah);
        jumlah = a * b;
        System.out.println("a * b = " + jumlah);
        jumlah = a / b;
        System.out.println("a / b = " + jumlah);
        jumlah = a % b;
        System.out.println("a % b = " + jumlah);
    }
}
